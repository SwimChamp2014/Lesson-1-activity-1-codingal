class India():
    def capital(self):
        print("The capital of India is New Delhi. ")
    def language(self):
        print("The most popular language in India is Hindi. ")
    def type(self):
        print("India is currently a developing country. ")
    def population(self):
        print("The population of India is 1.5 billion. ")

class USA():
    def capital(self):
        print("The capital of the US is Washington D.C. ")
    def language(self):
        print("The most widely spoken language in the US is english. ")
    def type(self):
        print("The US is a developed country. ")
    def population(self):
        print("The population of the US is 330 million. ")

US = USA()
India = India()

US.capital()
US.language()
US.type()
US.population()

